package np.edu.scst.operator;
public class Arithmetic {
    public static void main(String[] args) {
        // + = operator, 10,7 are operand
        int a = 10+7;
        System.out.println(a);

        a=10-7;        
        System.out.println(a);

        a=10*7;        
        System.out.println(a);
        
        a=10/7;
        System.out.println(a);

        a=10%7; //modulus or remainder //1
        System.out.println(a);
    }
}
